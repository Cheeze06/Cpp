// C++ 소개 및 C++의 장점과 특징

// C++은 C언어를 기반으로 한 프로그래밍 언어로, C언어의 장점에 객체 지향 프로그래밍을 추가하여 더욱 좋고 다양한 기능을 제공한다.

// 객체 지향 프로그래밍(OOP) : C++은 객체 지향 프로그래밍을 지원한다.
// 프로그래밍을 더 모듈화 하고 유지보수하기 쉽게 만들어준다.
// 클래스와 객체를 사용하여 데이터와 기능을 하나로 묶을 수 있다.

// 재사용성 : C++ 에서는 코드를 모듈화하고 클래스를 만들어 재사용이 가능해진다.
// 이미 작성한 클래스를 다른 프로젝트에서도 활용할 수 있어 개발 시간을 단축시켜준다.

// 효율성 : C++은 C언어와 함께 하드웨어에 가까운 접근이 가능하다.
// 메모리 사용량이 효율적이며, 고성능 응용 프로그램을 갭라할 수 있다.

// STL(standard Template Library) : C++ 에는 STL이라는 표준 라이브러리가 존재한다.
// 다양한 자료 구조와 알고리즘을 제공하여 프로그래밍을 훨씬 간편하게 만들어준다.

// C 호환성 : C++ 은 C와 호완이 가능.
// C로 작성된 코드를 C++로 확장하거나 C++코드를 C로 변환할 수 있다.

